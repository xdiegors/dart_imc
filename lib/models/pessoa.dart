class Pessoa {
  String _nome = "";
  double _peso = 0;
  double _altura = 0;

  getNome() {
    return _nome;
  }

  setNome(nome) {
    _nome = nome;
  }

  getPeso() {
    return _peso;
  }

  setPeso(peso) {
    _peso = peso;
  }

  getAltura() {
    return _altura;
  }

  setAltura(altura) {
    _altura = altura;
  }
}
